    private void deleteAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAccountActionPerformed
        String pass = JOptionPane.showInputDialog(this,
                "<html><b>Enter your password to continue</b></html>",
                "Success",
                JOptionPane.INFORMATION_MESSAGE);

        if (!pass.equals(MainUser.user.getPassword())) {
            JOptionPane.showMessageDialog(this,
                    "<html><b>Wrong Password</b></html>",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        int choice = JOptionPane.showConfirmDialog(this,
                "<html><b>Permanently delete account?</b></html>",
                "Confirm Delete Action",
                JOptionPane.YES_NO_OPTION);

        switch (choice) {
            case JOptionPane.YES_OPTION:
                try {
                    // disconnecting user
                    MainUser.proxy.disconnect(MainUser.user.getIduser());

                    MainUser.loggedIN = false;

                    // adjust the view
                    MainUser.chatBtn.setVisible(false);

                    MainUser.groupBtn.setBackground(MyColors.LIGHT_BLUE);
                    MainUser.groupBtn.setVisible(false);

                    MainUser.broadcastBtn.setBackground(MyColors.LIGHT_BLUE);
                    MainUser.broadcastBtn.setVisible(false);

                    MainUser.settingBtn.setBackground(MyColors.LIGHT_BLUE);
                    MainUser.settingBtn.setVisible(false);

                    MainUser.logoutBtn.setVisible(false);
                    MainUser.homeBtn.setVisible(true);

                    // delete old userChatFrames
                    MainUser.userChatFrames.values().forEach((frame) -> {
                        frame.dispose();
                    });

                    // delete old groupChatFrames
                    MainUser.groupChatFrames.values().forEach((frame) -> {
                        frame.dispose();
                    });

                    MainUser.userChatFrames.clear();
                    MainUser.groupChatFrames.clear();
                    MainUser.desk2.removeAll();

                    // clear display frames
                    MainUser.allUsersFrame.clearJList();
                    MainUser.allGroupsFrame.clearJList();

                    MainUser.loginFrame.setVisible(true);

                    // delete user groups from server
                    for (int groupId : MainUser.userGroups.keySet()) {
                        int adminId = GroupController.INSTANCE.findById(groupId).getAdminId();
                        if (adminId == MainUser.user.getIduser()) {
                            // user is group admin -> delete group for all
                            // send notice to all group members
                            String noticeMessage = " *** Group deleted by Admin ***\n";
                            String noticeIdComb = groupId + "-" + MainUser.user.getIduser();
                            MainUser.proxy.sendToGroup(noticeMessage, noticeIdComb);

                            // remove group for all users (server)
                            for (int userId : MainUser.proxy.getGroupUsersIDs(groupId)) {
                                String idComb = groupId + "-" + userId;
                                MainUser.proxy.deleteGroup(idComb);
                            }

                            // delete group (db)
                            GroupController.INSTANCE.delete(groupId);
                        } else {
                            // not admin -> leave group
                            String noticeMessage = " *** " + MainUser.user.getFirstName() + " " + MainUser.user.getLastName() + " left the group ***";
                            String noticeIdComb = groupId + "-" + MainUser.user.getIduser();
                            MainUser.proxy.sendToGroup(noticeMessage, noticeIdComb);

                            // remove user from group (server)
                            String idComb = groupId + "-" + MainUser.user.getIduser();
                            MainUser.proxy.deleteGroup(idComb);

                            // remove user from group (db)
                            UserGroupController.INSTANCE.removeUserFromGroup(MainUser.user.getIduser(), groupId);
                        }
                    }

                    // clear user groups locally 
                    MainUser.userGroups.clear();

                    // delete user from db
                    UserController.INSTANCE.delete(MainUser.user.getIduser());
//                    UserGroupController.INSTANCE.removeUserGroups(MainUser.user.getIduser());

                    this.setTitle("BChat - Login");

                    JOptionPane.showMessageDialog(this,
                            "<html><b>Account deleted successfully</b></html>",
                            "Success",
                            JOptionPane.ERROR_MESSAGE);

                } catch (RemoteException | SQLException ex) {
                    System.out.println(ex.getMessage());
                }

                break;

            case JOptionPane.NO_OPTION:
                break;

            default:
                break;
        }
    }//GEN-LAST:event_deleteAccountActionPerformed


