    private void editPassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editPassActionPerformed
        String newPass = password.getText();
        if (newPass.equals(MainUser.user.getPassword())) {
            JOptionPane.showMessageDialog(this,
                    "<html><b>Nothing to change</b></html>",
                    "Entry Alert",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        int choice = JOptionPane.showConfirmDialog(this,
                "<html><b>Change password?</b></html>",
                "Confirm Edit Action",
                JOptionPane.YES_NO_OPTION);

        switch (choice) {
            case JOptionPane.YES_OPTION:
                try {
                    UserController.INSTANCE.update(MainUser.user.getUsername(), newPass, MainUser.user.getIduser());
                    MainUser.user.setPassword(newPass);
                    JOptionPane.showMessageDialog(this,
                            "<html><b>Password changed successfully!</b></html>",
                            "Success",
                            JOptionPane.INFORMATION_MESSAGE);
                } catch (SQLException ex) {
                    System.out.println(ex.getMessage());
                }
                break;

            case JOptionPane.NO_OPTION:
                password.setText(MainUser.user.getPassword());
                break;

            default:
                break;
        }
    }//GEN-LAST:event_editPassActionPerformed

